$(function(){

	/* ---------------------------------------------------------------------------------------------------------------
	------------------------------------------------------------------------------------------------------------------
	------ GENERAL
	------------------------------------------------------------------------------------------------------------------
	---------------------------------------------------------------------------------------------------------------- */

	// Abrir y cerrar men√∫ lateral
	$('.js-btnMenu').on('click', function() {
		$(this).toggleClass('material--arrow');
		$('body').toggleClass('menuOpen');
	});


	// Cambiar de idioma
	$('.lang').on('click', '.js-lang', function(event) {
		event.preventDefault();
		$.ajax({
			url      : base_ajax + 'changelang',
			type     : 'post',
			dataType : 'json',
			data     : {
				lang : $(this).data('lang'),
				url  : $(this).attr('href')
			}
		})
		.done(function(response) {
			if (response.code == 200) {
				window.location.href = response.url;
			}
		})
		.fail(function() {
			console.log("error");
		});
	});

});
